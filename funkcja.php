<?php

function dodawanie($liczbaa, $liczbab) {
    echo $liczbaa + $liczbab;
}

function odejmowanie($liczbaa, $liczbab) {
    echo $liczbaa - $liczbab;
}

function mnożenie($liczbaa, $liczbab) {
    echo $liczbaa * $liczbab;
}

function dzielenie($liczbaa, $liczbab) {
    echo $liczbaa / $liczbab;
}

?>